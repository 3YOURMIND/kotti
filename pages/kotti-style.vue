# Kotti-Style CSS Library

Based on Kotti Design System, we created `Kotti-Style` CSS library which is customized and favored 
CSS framework forked from [Spectre.css](https://picturepan2.github.io/spectre/index.html).


## Installation

**Intall from yarn**

```sh
yarn add @3yourmind/kotti-style
```

**Intall from npm**

```sh
npm install -D @3yourmind/kotti-style
```

**Import Kotti-Style in your project**

```scss
@import "@3yourmind/kotti-style
```

