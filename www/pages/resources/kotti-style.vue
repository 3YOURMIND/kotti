# Kotti-Style CSS Library

The `kotti-style` CSS library, based on the Kotti Design System, is a highly customized
fork of [Spectre.css](https://picturepan2.github.io/spectre/index.html).

## Installation

**yarn**: `yarn add @3yourmind/kotti-style`

**npm**: `npm install @3yourmind/kotti-style`

## Import

```scss
@import "@3yourmind/kotti-style";
```

